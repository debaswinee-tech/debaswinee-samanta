#!/bin/bash
# Simple Interest Calculation
P=1000  # Principal Amount
R=5     # Rate of Interest (in %)
T=2     # Time (in years)

# Calculate Simple Interest (SI)
SI=$(( (P * R * T) / 100 ))

echo "The calculated Simple Interest is: $SI"
